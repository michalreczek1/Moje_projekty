package sklep.db;

public class SklepException extends Exception {

	public SklepException() {
		super();
	}

	public SklepException(String message, Throwable cause) {
		super(message, cause);
	}

	public SklepException(String message) {
		super(message);
	}

	public SklepException(Throwable cause) {
		super(cause);
	}
	
}
